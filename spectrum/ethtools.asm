STREAM:	DEFW	WRITE
	DEFW	READ
	DEFB	"H"
	DEFW	WRITE
	DEFW	READ
	DEFW	STREAME- STREAM
STREAME:EQU	$
VARADD:	DEFM	"A$"
	DEFB	0x0D
VARKEY:	DEFM	"K$"
	DEFB	0x0D
	INCLUDE	"../keccaktab.asm"
